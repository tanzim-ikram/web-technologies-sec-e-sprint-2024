<?php 
    // PHP script to calculate the area and perimeter of a Rectangle
    $length = 4;
    $width = 5;

    $area = $length * $width;
    $perimeter = 2 * ($length + $width);

    echo "Length = " . $length . "<br>";
    echo "Width = " . $width . "<br>";
    echo "The area of the Rectangle = " . $area . "<br>";
    echo "The perimeter of the Rectangle = " . $perimeter . "<br>";

?>